    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        //Codigo Para ver quantas repetições teve as datas
        boolean achei;
        for(int i = 0; i<acesso.size(); i++){
            achei = false;
            for(int j = 0; j < vetData.size(); j++){
                if(acesso.get(i).getData().equalsIgnoreCase(vetData.get(j))){
                    achei = true;
                }
            }
            if(!achei){
                vetData.add(acesso.get(i).getData());
            }
        }
        int qtd;
        for(int i = 0; i < vetData.size(); i++ ){
           qtd = 0;
           for(int j = 0; j < acesso.size(); j++ ){
               if(vetData.get(i).equalsIgnoreCase(acesso.get(j).getData())){
                   qtd++;
               }
           }
           vetQTD.add(qtd);
        }
        Integer[] xi = vetQTD.toArray(new Integer[vetQTD.size()]);
        Arrays.sort(xi);
        //Codigo para ver quantas vezes o numero de repetição da data teve
        
        for(int i = 0; i<xi.length; i++){
            achei = false;
            for(int j = 0; j < vetQTDN.size(); j++){
                if(xi[i] == vetQTDN.get(j)){
                    achei = true;
                }
            }
            if(!achei){
                vetQTDN.add(xi[i]);
            }
        }
        for(int i = 0; i < vetQTDN.size(); i++ ){
           qtd = 0;
           for(int j = 0; j < xi.length; j++ ){
               if(vetQTDN.get(i) == xi[j]){
                   qtd++;
               }
           }
           vetfi.add(qtd);
        }
        double frequencia;
        int valor = 0;
        int so = 0;
        for (int i = 0; i < vetfi.size(); i++) {
           valor = vetfi.get(i);
           so = so+valor;
        }
        for (int i = 0; i < vetfi.size(); i++) {
            frequencia = vetfi.get(i)*100 / (double)so;
            vetFreq.add(frequencia);
        }
        
        //Calculo do xi.fi
        int resultado;
        for (int i = 0; i < vetQTDN.size(); i++) {
            resultado = vetQTDN.get(i) * vetfi.get(i);
            vetxifi.add(resultado);
        }
        
        //Calculo do Fi
        int primeiro = 0;
        int numeros;
        for (int i = 0; i < vetfi.size(); i++) {
            numeros = primeiro + vetfi.get(i);
            primeiro = numeros;
            vetFi.add(numeros);
        }
        
        //Calculo do Fr
        double prime = 0;
        double resto = 0;
        for (int i = 0; i < vetFreq.size(); i++) {
            resto = prime + vetFreq.get(i);
            prime = resto;
            vetFr.add(resto);
        }
        
        //Calculo da Media
        
        int soma1 = 0,so1 = 0;
        double media;
        for (int i = 0; i < vetxifi.size(); i++) {
            soma1 = soma1 + vetxifi.get(i);
            so1 = so1+vetfi.get(i);
        }
        media = (double)soma1 / so1;
        
        //Calcular o (xi-Media)^2.fi
        
        double mult = 0;
        for (int i = 0; i < vetfi.size(); i++) {
            mult = Math.pow(vetQTDN.get(i) - media, 2)*vetfi.get(i);
            vetV.add(mult);
        }        
        for (int i = 0; i < vetQTDN.size(); i++) {
            DefaultTableModel tabela = (DefaultTableModel) tTabeladeDados.getModel();
            tabela.addRow(new String[] {String.valueOf(vetQTDN.get(i)),String.valueOf(vetfi.get(i)),String.valueOf(vetFi.get(i)),String.format("%.2f", vetFreq.get(i))+ String.valueOf("%"),String.format("%.2f", vetFr.get(i))+ String.valueOf("%"),String.valueOf(vetxifi.get(i)),String.format("%.2f", vetV.get(i))});
        }
        
        
    }//GEN-LAST:event_btnListarActionPerformed

