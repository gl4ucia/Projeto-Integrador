    private void btnMedianaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMedianaActionPerformed
        int resu;
        int posi = 0;
        if(vetQTDN.size() % 2 != 0){
            posi = ((vetQTDN.size()+1)/2);
            resu = vetQTDN.get(posi-1);
        }else{
            posi = Math.round((vetQTDN.size()+1)/2)-1;
            resu = (vetQTDN.get(posi)+vetQTDN.get(posi+1))/2;
        }
        jMediana.setText(String.valueOf(resu));
    }//GEN-LAST:event_btnMedianaActionPerformed

