    private void btnArquivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnArquivoActionPerformed
        JFileChooser filechoose = new JFileChooser();
        filechoose.setDialogTitle("Procurar Arquivo Log");
        filechoose.setFileSelectionMode(JFileChooser.FILES_ONLY);
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Texto", "txt","csv");
        filechoose.setFileFilter(filter);
        int retorno = filechoose.showOpenDialog(this);
        
        if(retorno == JFileChooser.APPROVE_OPTION){
            
            File file = filechoose.getSelectedFile();            
            jCaminho.setText(file.getPath());
            try {
                Scanner luis = new Scanner(file);
                tArea.read( new FileReader( file.getAbsolutePath() ), null );
                
                // Adicionar Novos Acessos
                
                String linhaArquivo;
                String [] dadosAcesso;
                while(luis.hasNext()){
                  Acessos acessosLog = new Acessos();
                  linhaArquivo = luis.nextLine();
                  dadosAcesso = linhaArquivo.split(";");
                  acessosLog.setData(dadosAcesso[0]);
                  acessosLog.setHora(dadosAcesso[1]);
                  acessosLog.setSistema(dadosAcesso[2]);
                  acessosLog.setNavegador(dadosAcesso[3]);
                  acesso.add(acessosLog);
                  
                                    
                }
                 
            }catch (IOException ex) {
                System.out.println("problem accessing file"+file.getAbsolutePath());
            }
            
            
        }
    }//GEN-LAST:event_btnArquivoActionPerformed

